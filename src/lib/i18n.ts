export function formatRelativeTime(ts: number, now: number, lang: Lang): string {
  const ms = Math.max(0, now - ts);
  const min = Math.floor(ms / 60_000);
  if (min < 1) return lang === "ar" ? "الآن" : "just now";
  if (min < 60)
    return lang === "ar"
      ? `منذ ${min} دقيقة`
      : `${min} min ago`;
  const hr = Math.floor(min / 60);
  if (hr < 24)
    return lang === "ar"
      ? `منذ ${hr} ساعة`
      : `${hr} hr ago`;
  const day = Math.floor(hr / 24);
  return lang === "ar"
    ? `منذ ${day} يوم`
    : `${day} day${day === 1 ? "" : "s"} ago`;
}

export function getLang(cookieValue: string | undefined): Lang {
  return cookieValue === "ar" ? "ar" : "en";
}
